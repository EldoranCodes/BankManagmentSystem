    private void closeAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeAccountButtonActionPerformed
        // TODO add your handling code here:
        
        int response = JOptionPane.showConfirmDialog(null,"Are you sure you want to close your account?","Be Advised",JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.OK_OPTION){
           //check the account
           if (controller.loggedInAccount.getAccountBalance() != 0.0){
               JOptionPane.showMessageDialog(null, "Account Balance must be 0","Closed Account", JOptionPane.INFORMATION_MESSAGE);
               return;
           }
           String result =  controller.closeAccount();
           if (result.contains("successfully")){
               JOptionPane.showMessageDialog(null, result,"Closed Account", JOptionPane.INFORMATION_MESSAGE);
               LogInForm loginForm= new LogInForm(controller);
               loginForm.setVisible(true);
               dispose();
           }else JOptionPane.showMessageDialog(null, result,"Closed Account", JOptionPane.ERROR_MESSAGE);

        }else return;
        
      
        // Account Balance must be 0;
        
        
    }//GEN-LAST:event_closeAccountButtonActionPerformed

