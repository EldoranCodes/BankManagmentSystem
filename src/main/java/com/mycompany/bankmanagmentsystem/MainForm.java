    private void changePINButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePINButtonActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(null, "Enter your new PIN:", "Input", JOptionPane.QUESTION_MESSAGE);
        //checks if digit only and 6 digit
        
        if (input == null){  
            return; 
        }
        
        if (!input.matches("[-+]?\\d*\\.?\\d+")){  
            JOptionPane.showMessageDialog(null,"PIN must be a number only","Change PIN",JOptionPane.ERROR_MESSAGE);
            return; 
        }
        
        if (input.length() <= 5){
           JOptionPane.showMessageDialog(null,"PIN must be 6 digits or more","Change PIN",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String input2 = JOptionPane.showInputDialog(null, "Verify your new PIN: ", "Input", JOptionPane.QUESTION_MESSAGE);
        if (input2 == null){  
            return; 
        }
        if(!input2.equals(input) ){
            JOptionPane.showMessageDialog(null," PIN Change error! \n You entered a wrong PIN, it must be the same as the new PIN","Verify PIN",JOptionPane.ERROR_MESSAGE);
            return; 
        }
        //parsae the string to int then change the PIN
        int newPIN = Integer.parseInt(input);
        
        String result =  controller.changePin(newPIN);
        
        if (result.contains("Successfull")){
            JOptionPane.showMessageDialog(null,result,"Change PIN",JOptionPane.INFORMATION_MESSAGE);
        } else JOptionPane.showMessageDialog(null,"Error Changing PIN","Change PIN",JOptionPane.ERROR_MESSAGE);
        LogInForm loginForm  = new LogInForm(controller);
        loginForm.setVisible(true);
        dispose();
        
    }//GEN-LAST:event_changePINButtonActionPerformed

